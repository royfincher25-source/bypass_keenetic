#!/bin/sh
# =============================================================================
# ОТКЛЮЧЕНИЕ IPv6 НА KEENETIC
# =============================================================================
# Скрипт автозапуска для отключения IPv6
# Установка: /opt/etc/ndm/100-disable-ipv6.sh
# =============================================================================

[ "$1" != "start" ] && exit 0

# Отключение IPv6
sysctl -w net.ipv6.conf.all.disable_ipv6=1
sysctl -w net.ipv6.conf.default.disable_ipv6=1

# Проверка
echo "IPv6 отключён"
